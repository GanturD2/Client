bool TryGetAttributeFromString(const std::string& in, uint8_t& out)
{
	const std::string cmp = in.substr(6, in.length() - 6);
	for (const auto& pair : c_aApplyTypeNames)
	{
		if (cmp.compare(pair.c_pszName) == 0) {
			out = pair.lValue;
			return true;
		}
	}

	return false;
}

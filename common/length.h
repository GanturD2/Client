typedef struct SItemPos
{
	uint8_t window_type;
	uint16_t cell;
	SItemPos() noexcept
	{
		window_type = INVENTORY;
		cell = WORD_MAX;
	}

	SItemPos(uint8_t _window_type, uint16_t _cell) noexcept
	{
		window_type = _window_type;
		cell = _cell;
	}

	bool IsValidItemPosition() const noexcept
	{
		switch (window_type)
		{
			case RESERVED_WINDOW:
				return false;

			case INVENTORY:
				return cell < (INVENTORY_SLOT_COUNT);

			case EQUIPMENT:
				return cell < (EQUIPMENT_SLOT_COUNT);

			case BELT_INVENTORY:
				return cell < (BELT_INVENTORY_SLOT_COUNT);

			case DRAGON_SOUL_INVENTORY:
				return cell < (DRAGON_SOUL_INVENTORY_MAX_NUM);

#ifdef ENABLE_SWITCHBOT
			case SWITCHBOT:
				return cell < (SWITCHBOT_SLOT_COUNT);
#endif

#ifdef ENABLE_ATTR_6TH_7TH
			case NPC_STORAGE:
				return cell < (NPC_STORAGE_SLOT_COUNT);
#endif

#ifdef ENABLE_ADDITIONAL_EQUIPMENT_PAGE
			case ADDITIONAL_EQUIPMENT_1:
				return cell < (ADDITIONAL_EQUIPMENT_MAX_SLOT);
#endif

			// A window that is dynamically sized cannot be checked for validity.
			case SAFEBOX:
			case MALL:
#ifdef ENABLE_GUILDSTORAGE_SYSTEM
			case GUILDBANK:
#endif
				return false;

			default:
				sys_err("Invalid window type %d", window_type);
				return false;
		}

		return false;
	}

	bool IsEquipPosition() const noexcept
	{
		return (IsEquipmentEquipPosition() || IsDragonSoulEquipPosition());
	}
	bool IsEquipmentEquipPosition() const noexcept
	{
		return (EQUIPMENT == window_type && cell >= 0 && cell < WEAR_MAX_NUM);
	}
	bool IsDragonSoulEquipPosition() const noexcept
	{
		return (EQUIPMENT == window_type && cell >= DRAGON_SOUL_EQUIP_SLOT_START && cell < DRAGON_SOUL_EQUIP_SLOT_END);
	}
	bool IsBeltInventoryPosition() const noexcept
	{
		return (BELT_INVENTORY == window_type && cell >= BELT_INVENTORY_SLOT_START && cell < BELT_INVENTORY_SLOT_END);
	}
	bool IsDefaultInventoryPosition() const noexcept
	{
		return (INVENTORY == window_type && cell < INVENTORY_MAX_NUM);
	}

#ifdef ENABLE_SWITCHBOT
	bool IsSwitchbotPosition() const noexcept
	{
		return ((SWITCHBOT == window_type) && (cell < SWITCHBOT_SLOT_COUNT));
	}
#endif

#ifdef ENABLE_SPECIAL_INVENTORY
	bool IsSpecialInventoryPosition() const noexcept
	{
		return ((INVENTORY == window_type) && (cell >= SPECIAL_INVENTORY_SLOT_START && cell < SPECIAL_INVENTORY_SLOT_END));
	}

	int GetSpecialInventoryType() noexcept
	{
		if ((cell >= SPECIAL_INVENTORY_SKILLBOOK_START) && (cell < SPECIAL_INVENTORY_SKILLBOOK_END))
		{
			return SPECIAL_INVENTORY_TYPE_SKILLBOOK;
		}
		else if ((cell >= SPECIAL_INVENTORY_STONE_START) && (cell < SPECIAL_INVENTORY_STONE_END))
		{
			return SPECIAL_INVENTORY_TYPE_STONE;
		}
		else if ((cell >= SPECIAL_INVENTORY_MATERIAL_START) && (cell < SPECIAL_INVENTORY_MATERIAL_END))
		{
			return SPECIAL_INVENTORY_TYPE_MATERIAL;
		}
		else
		{
			return -1;
		}
	}
#endif

#ifdef ENABLE_ADDITIONAL_EQUIPMENT_PAGE
	bool IsAdditionalEquipment1() const noexcept
	{
		return ((ADDITIONAL_EQUIPMENT_1 == window_type) && (cell < ADDITIONAL_EQUIPMENT_MAX_SLOT));
	}
#endif

	bool operator == (const struct SItemPos& rhs) const noexcept
	{
		return ((window_type == rhs.window_type) && (cell == rhs.cell));
	}

//#ifdef ENABLE_PREMIUM_PRIVATE_SHOP
	bool operator != (const struct SItemPos& rhs) const noexcept
	{
		return ((window_type != rhs.window_type) || (cell != rhs.cell));
	}
//#endif

	bool operator < (const struct SItemPos& rhs) const noexcept
	{
		return ((window_type < rhs.window_type) || ((window_type == rhs.window_type) && (cell < rhs.cell)));
	}
} TItemPos;

